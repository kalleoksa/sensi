// Advance per glyph (cell + gap) at a given scale.
export function advance(scale = 1): number {
  return (GW + GAP) * scale;
}

// Pixel width of a string when drawn at scale (no trailing gap).
export function measure(text: string, scale = 1): number {
  if (text.length === 0) return 0;
  return text.length * advance(scale) - GAP * scale;
}

export const GLYPH_H = GH;

// Draw uppercase text. Lowercase is upper-cased; unknown glyphs render blank.
export function drawText(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color: string,
  scale = 1,
): void {
  ctx.fillStyle = color;
  let cx = x;
  for (const ch of text.toUpperCase()) {
    const glyph = GLYPHS[ch];
    if (glyph) {
      for (let r = 0; r < GH; r++) {
        const row = glyph[r];
        for (let c = 0; c < GW; c++) {
          if (row[c] === '1') ctx.fillRect(cx + c * scale, y + r * scale, scale, scale);
        }
      }
    }
    cx += advance(scale);
  }
}

// Convenience: draw centered within [x, x+width).
export function drawTextCentered(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  width: number,
  y: number,
  color: string,
  scale = 1,
): void {
  const w = measure(text, scale);
  drawText(ctx, text, Math.round(x + (width - w) / 2), y, color, scale);
}
